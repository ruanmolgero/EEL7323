#include "float_operations.h"
#include <iostream>
using namespace std;

float addition(float n1, float n2)
{
	return n1 + n2;
}

float subtraction(float n1, float n2)
{
	return n1 - n2;
}

float multiplication(float n1, float n2)
{
	return n1 * n2;
}

float division(float n1, float n2)
{
	return n1 / n2;
}
