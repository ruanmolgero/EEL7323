#include "operations.h"
#include <iostream>
using namespace std;

int addition(int n1, int n2)
{
	return n1 + n2;
}

int subtraction(int n1, int n2)
{
	return n1 - n2;
}

int multiplication(int n1, int n2)
{
	return n1 * n2;
}

int division(int n1, int n2)
{
	return n1 / n2;
}
